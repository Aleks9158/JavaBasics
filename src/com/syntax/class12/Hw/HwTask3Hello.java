package com.syntax.class12.Hw;

public class HwTask3Hello {
/*
 * Create a method that will say Hello in different
 * country that will passed when method is executed
 */
	void printHello(String country) {
		switch(country) {
		case "USA":
		System.out.println("Hello");
			break;	
			
		case "Germany":
		System.out.println("Hello");
		break;
		
		case "Afg":
		System.out.println("Salam");
		break;
		
		case "Turkey":
		System.out.println("Merhaba");
		break;
}
	}
}
