package com.syntax.class07;

public class HwForLoop1 {

	public static void main(String[] args) {
		//Print numbers from 1 to 100 in 1 line with space
		//Print numbers from 100 to 1
       int i=1;
       for(i=1;i<=100;i++) {
    	   System.out.print(i+" ");
       }
        System.out.print("\n");
     
       for(i=100;i>=1;i--) {
    	   System.out.print(i+" ");
	}

}}
