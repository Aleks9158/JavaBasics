package com.syntax.class07;

public class HwForLoopEven {

	public static void main(String[] args) {
		// Print even numbers from 20 to 1 (2 ways)
		
		int j=20;
	         for(j=20;j>=1;j--) {
	    	  if(j%2==0) {
	    	 System.out.print(j+" ");
	       }
	         }
	    System.out.println("\n");
	    int	i=20;
	        do {
	    		System.out.print(i+" ");
	    		i-=2;
	    	}while(i>=1);
	    	 
	}
}
	
	
	



